package com.kai.pojo;

public class TiaoJian {
    private String ming;
    private String YanYuan;
    private String LeixingId;
    private String DiJia;

    public String getMing() {
        return ming;
    }

    public void setMing(String ming) {
        this.ming = ming;
    }

    public String getYanYuan() {
        return YanYuan;
    }

    public void setYanYuan(String yanYuan) {
        YanYuan = yanYuan;
    }

    public String getLeixingId() {
        return LeixingId;
    }

    public void setLeixingId(String leixingId) {
        LeixingId = leixingId;
    }

    public String getDiJia() {
        return DiJia;
    }

    public void setDiJia(String diJia) {
        DiJia = diJia;
    }
}
